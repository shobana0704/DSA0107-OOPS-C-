#include <iostream>
using namespace std;

int main()
{
    
    int a=10;
    float b=3.14f;
    double c=9.99;
    char d='A';
    bool e=true;

    
    
    cout <<"Int a = " << a <<endl;
    cout <<"Float b = " << b <<endl;
    cout <<"Double c = " << c <<endl;
    cout <<"Char d = " << d <<endl;
    cout <<"Bool e = " << e <<endl;
 return 0;
}